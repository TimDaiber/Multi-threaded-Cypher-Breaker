package ie.gmit.sw;

public class TopResult {
	double topReult = 0;
	double topKey = 0;
	String topMessage;

	public double getTopReult() {
		return topReult;
	}

	public void setTopReult(double topReult) {
		this.topReult = topReult;
	}

	public double getTopKey() {
		return topKey;
	}

	public void setTopKey(double topKey) {
		this.topKey = topKey;
	}

	public String getTopMessage() {
		return topMessage;
	}

	public void setTopMessage(String topMessage) {
		this.topMessage = topMessage;
	}
}
